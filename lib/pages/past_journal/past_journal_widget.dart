class PastJournalWidget extends StatefulWidget {
  const PastJournalWidget({super.key});

  @override
  State<PastJournalWidget> createState() => _PastJournalWidgetState();
}

class _PastJournalWidgetState extends State<PastJournalWidget> {
  late PastJournalModel _model;

  final scaffoldKey = GlobalKey<ScaffoldState>();

  @override
  void initState() {
    super.initState();
    _model = createModel(context, () => PastJournalModel());
  }

  @override
  void dispose() {
    _model.dispose();

    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    context.watch<FFAppState>();

    return GestureDetector(
      onTap: () => _model.unfocusNode.canRequestFocus
